#6.1 Спортсмен 1 вариант задачи, в которой требуется найти день, на который ОБЩИЙ(в сумме) результат за все дни будет равен
#необходимому значению
a = int(input("Введите расстояние в км, которое Вы пробежали в первый день: "))
b = int(input("Введите расстояние в км, которое Вы хотите пробежать за все дни суммарно: "))
day = 1
if b>a:
    while a < b:
        a = a + a*1.1
        day += 1
        print(f'К {day} дню Вы пробежали {a:.2f} километров в сумме')
    print(f'Вам понадобится {day} дней, чтобы суммарное расстояние равнялось {b} км')
else: print('Вы уже достигли нужного расстояния в первый день!')
