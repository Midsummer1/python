#2. Форматирование строк, перевод секунд в формат чч:мм:сс
sec = int(input("Введите время в секундах: "))#изначальные секунды
hours = sec // 3600 #количество целых часов в изначальных секундах
minutes = (sec - hours * 3600) // 60 #количество целых минут в изначальных секундах за вычетом секунд,
# которые приходятся на целые часы
seconds = sec - hours * 3600 - minutes * 60 #количество секунд в изначальных секундах без целых минут
# и целых часов
print(f'Результат - {hours:02d}:{minutes:02d}:{seconds:02d}')
